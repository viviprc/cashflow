<template>
  <v-container>
    <v-card>
      <v-card-title>
        <v-text-field
          v-model="search"
          append-icon="mdi-magnify"
          label="Buscar"
          single-line
          hide-details
        ></v-text-field>
      </v-card-title>
      <v-data-table
        :headers="headers"
        :items="parsedSales"
        :search="search"
        sort-by="calories"
        class="elevation-1"
      >
        <template v-slot:top>
          <v-toolbar flat>
            <v-toolbar-title>Historial de Ventas</v-toolbar-title>
            <v-divider class="mx-4" inset vertical></v-divider>
            <v-spacer></v-spacer>
          </v-toolbar>
        </template>
        <template v-slot:item.actions="{ item }">
          <v-icon small class="d-flex ml-3" @click="goToDetail(item)">
            mdi-eye-plus
          </v-icon>
        </template>
      </v-data-table>
    </v-card>
  </v-container>
</template>
<script>
import ParseDate from "../helpers/date";
import { mapState, mapActions } from "vuex";

export default {
  name: "SalesTable",
  data: () => ({
    search: "",
    dialog: false,
    headers: [
      {
        text: "Fecha",
        align: "start",
        sortable: true,
        value: "data.date",
      },
      { text: "Modo de pago", value: "data.type" },
      { text: "Total", value: "data.total" },
      { text: "Actions", value: "actions", sortable: false },
    ],
  }),

  computed: {
    ...mapState(["sales"]),
    parsedSales() {
      return this.sales.map((s) => ({
        ...s,
        data: {
          ...s.data,
          date: ParseDate(s.data.date),
        },
      }));
    },
  },
  methods: {
    ...mapActions(["removeSale"]),
    ParseDate,
    goToDetail(item) {
      this.$router.push(`sales_history/${item.id}`);
    },
  },
};
</script>